// =============================================================================
//Helper class for storing coordinates
public class Pair {
// =============================================================================

    // =============================================================================
    // FIELDS
    int x; //stored x position
    int y; //stored y position
    // =============================================================================

    // =============================================================================
     /**
     * The constructor, stores paramaters in instance
     *
     * @param x The x position toStore.
     * @param y The y position toStore
     *
     **/
    public Pair(int x, int y) {
	this.x = x;
	this.y = y;
    }// Pair ()
    // =============================================================================

// =============================================================================
}
// =============================================================================
